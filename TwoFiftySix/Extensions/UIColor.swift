import UIKit

// https://stackoverflow.com/questions/24263007/how-to-use-hex-color-values
extension UIColor {
    /// Initialize a UIColor using integer RGB values (to 255).
    convenience init(red: Int, green: Int, blue: Int, alpha: CGFloat = 1.0) {
        self.init(
            red: CGFloat(red) / 255.0,
            green: CGFloat(green) / 255.0,
            blue: CGFloat(blue) / 255.0,
            alpha: alpha
        )
    }
    /// Initialize a UIColor using a single integer (expected to be three rgb hex values).
    convenience init(rgb: Int, alpha: CGFloat = 1.0) {
        self.init(
            red: (rgb >> 16) & 0xFF,
            green: (rgb >> 8) & 0xFF,
            blue: rgb & 0xFF,
            alpha: alpha
        )
    }
}
